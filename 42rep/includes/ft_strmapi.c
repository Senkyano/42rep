#include "libft.h"

char	*ft_strmapi(char const *s, char (f*)(unsigned int, char))
{
	size_t			len;
	unsigned int	i;
	char			*str;

	len = ft_strlen(s);
	str = malloc((len + 1) * sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
